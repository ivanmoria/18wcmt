let currentLang = 'en';

function setLang(lang) {
  currentLang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (T[lang] && T[lang][key] !== undefined) {
      el.innerHTML = T[lang][key];
    }
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (T[lang] && T[lang][key]) el.placeholder = T[lang][key];
  });
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });
  // Sync topbar toggle button flag if function exists
  if(typeof updateLangToggle === 'function') updateLangToggle(lang);
  try { localStorage.setItem('wcmt_lang', lang); } catch(e) {}
}

function initLang() {
  let lang = 'en';
  try { lang = localStorage.getItem('wcmt_lang') || 'en'; } catch(e) {}
  setLang(lang);
}
